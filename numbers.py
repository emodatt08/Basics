
import math
#divisions will always output quotient
divide = 7/5
print(divide)

#getting the remainder of a division
remainder = 7%5
print(remainder)

#power of a number
square = 5 ** 2
print(square)

#absolute of a number
absolute = abs(-5)
print(absolute)

#power of 2
power = pow(5, 2)
print(power)

#float point value of decimal
floor = math.floor(5.8)
print(floor)


#shorthand
a = 13
b = 45
a+=b
print(a)